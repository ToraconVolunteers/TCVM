    private void populateForm() {
        String selection = Sys.UI.MainUI.getSelection();
        if (selection.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a volunteer first", "Error: No volunteer selected", JOptionPane.WARNING_MESSAGE);
            dispose();
        } else {
            int row = Sys.Core.File.ExcelReader.getRow(selection, Sys.Core.File.ExcelFilePrep.getNameCol());
            String[] name = Sys.UI.MainUI.getNameLabel(selection);
            firstNameLabel.setText(name[0]);
            lastNameLabel.setText(name[name.length-1]);
            emailLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getEmailCol(), row));
            mobileNumberLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getMobileCol(), row));
            statusLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getStatusCol(), row));
            hoursCompletedLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getCompletedCol(), row));
            //availabilityLabel.setText(Sys.Core.File.ExcelReader.getCellContent(9, row));
            //targetHoursLabel.setText(Sys.Core.File.ExcelReader.getCellContent(10, row));
            reimbursementLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getReimbursementCol(), row));
            //clubMemberLabel.setText(Sys.Core.File.ExcelReader.getCellContent(5, row));
            //preRegLabel.setText(Sys.Core.File.ExcelReader.getCellContent(6, row));
            shirtSizeLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getShirtSizeCol(), row));
            hoursDay1Label.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getTimeDay1Col(), row));
            hoursDay2Label.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getTimeDay2Col(), row));
            disabilityLabel.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getDisabilityCol(), row));
            noteTextArea.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getNoteCol(), row));
            scheduleTextArea.setText(Sys.Core.File.ExcelReader.getCellContent(ExcelFilePrep.getScheduleCol(), row));
        }
    }
    
    
